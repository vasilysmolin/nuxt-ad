          <h1 class="font-black text-black text-4xl text-center">Карельская сказка «Зять Хома»</h1>
          <hr>
          <blockquote>Благодаря сказкам народ осознаёт своё внутреннее достоинство и высмеивает собственные слабости —
            глупость, лень, упрямство и бесхозяйственность.
          </blockquote>
          <hr>
          <p>В стародавние времена, когда жители деревень небыли обучены грамоте, эстетические потребности
            удовлетворялись повествованием сказок, исполнением песен, шуток, загадок и прибауток. Шло время, народное устное
            творчество приобретало всё более совершенный вид и форму, становилось более глубоким по содержанию.
          </p>
          <p>Высоко ценилось умение рассказывать сказки, особенно этим славился карельский народ, где слава о хорошем
            сказочнике простиралась далеко за сотню вёрст. Сказочника звали в народе «старинщиком» и был он почётным
            гостем на свадьбах, деревенских праздниках и посиделках. Ценным кладом был сказочник в те времена, когда
            крестьяне уходили далеко от дома на дальние покосы, весеннюю или осеннюю рыбную ловлю, на зимние
            лесозаготовки. Сказочнику уступали самое тёплое место у костра, отдавали часть улова.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/259_2.png" alt=""/>
            <figcaption>Источник: Юфа Михаил Шлемович</figcaption>
          </figure>
          <p>Карельская сказка по сюжету близка к русским и финским сказкам, что объясняется культурно-историческими
            связями народов, живших испокон веков по соседству. Однако, несмотря на схожесть сюжетов, карельские сказки
            отличаются колоритом, характерными деталями быта, простой композицией и особенностями национального
            характера героев.
          </p>
          <p>Предлагаем к прочтению юмористическую сказку «Зять Хома», в которой ярко представлена типично карельская
            тональность мягкого юмора. Комизм сказки построен на том, что искреннее желание Хомы выполнить любую работу
            каждый раз заканчивается совершенно иным результатом, нежели планировал главный герой. Впрочем, не будем
            томить читателя, лучше прочтите сказку и сделайте выводы.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/259_3.png" alt=""/>
            <figcaption>Источник: Юфа Михаил Шлемович</figcaption>
          </figure>
          <p>Жили-были муж и жена. Мужа звали Хомой. Хоме всё кажется, что жене дома легко управляться с хозяйством, в
            тепле да под крышей. А он целыми днями то пашет, то дрова рубит. Говорит он жене:<br>
            — Вот что, жена, поди-ка теперь ты пахать, а с хозяйством дома и я справлюсь.<br>
            Жена в ответ:<br>
            — Я пойду, только ты все домашние дела справь к моему приходу: печь истопи, коров подои, хлеб испеки да
            ячмень смели. Вот тут сметана — масло сбей, кур выпусти, да гляди, чтоб коршун цыплят не унёс, поросёнка
            накорми… И за ребёнком смотри, чтоб не плакал.
          </p>
          <p>И ушла в поле пахать. Ей что, знай себе пашет. Хома стал хозяйничать, а сам думает: «Много же она мне работы
            оставила, за что приняться, чтобы всё успеть?» Затопил печь, подоил корову. Глядит, времени много прошло,
            солнце уже высоко. Давай скорее тесто месить. Взял квашню с печки, поставил на лавку — тут вспомнил про
            кур.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/259_4.png" alt=""/>
            <figcaption>Источник: Юфа Михаил Шлемович</figcaption>
          </figure>
          <p>Побежал в хлев, пустил кур в избу, насыпал зёрна, и двери оставил открытыми. А когда же масло сбивать и
            ячмень молоть? Придумал Хома: горшок со сметаной положил в кошель. Пока будет зерно молоть, масло само
            собьется. Тут ребёнок в люльке заплакал. Он его качать. А потом привязал верёвку — одним концом к люльке,
            другим — к своей ноге и пошёл в сени зерно на ручном жернове молоть. Крутит жернов, ногой люльку качает, в
            кошеле на спине масло сбивается. Всё вроде хорошо. Вдруг в избе куры закудахтали, ребёнок закричал.
          </p>
          <p>Бросился Хома в избу, споткнулся о порог, упал и всю сметану на себя пролил. Верёвка за ногу зацепилась, он
            дернул за неё — люлька перевернулась, ребёнок на пол упал, расшибся и пуще прежнего плачет. Совсем
            растерялся Хома. А весь переполох в избе поросёнок наделал. Забежал он в открытую дверь, квашню свалил с
            лавки, куры перепугались — в печь попрыгали — так и сгорели в печи, на горячих углях. Схватил Хома ребёнка,
            убаюкивает его, а сам весь в сметане. Остальные дети по углам разбежались, кто куда.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/259_5.png" alt=""/>
            <figcaption>Источник: Юфа Михаил Шлемович</figcaption>
          </figure>
          <p>— Авой-вой-вой,— причитает Хома,— что я наделал? Как я жене покажусь: ребёнка расшиб, сметану пролил, ячмень
            не смолол, куры сгорели, тесто поросёнок съел! Что делать мне, горемычному? А жена вот-вот обедать
            придёт!<br>
            Посмотрел в окно: тесть идёт, уже во дворе! Он скорей на полати, детям наказывает:<br>
            — Скажите дедушке, что отец и мать в поле ушли, пахать.<br><br>
            Дедушка пришёл, дети ему так и сказали, как отец велел. Дедушка всем гостинцы раздаёт:<br>
            — Это тебе, это тебе, это дайте матери, а вот это отцу. Хочется Хоме посмотреть, что тесть ему принёс, да
            ещё боится — вдруг дети его гостинец съедят? Ёрзал, ёрзал на полатях и свалился оттуда. Тесть говорит:<br>
            — Авой-вой-вой, Хома-зятюшка, что с тобой?<br>
            — Ой, тестюшка дорогой, и не спрашивай: остался я дома за хозяйку, да всё не так сделал,— и рассказывает он
            тестю, что наделал.<br>
            — А я пришёл тебя, зятюшка, на свадьбу звать!<br>
            — Ну, раз пришёл, так надо идти. Сбегай-ка за матерью,— говорит он старшему сыну.<br><br>
            Приходит хозяйка с поля, спрашивает:<br>
            — Где обед?<br>
            — Ох, женушка, ещё спрашиваешь про обед! Всё у меня не так: ребёнка расшиб, куры сгорели, тесто свинья
            съела, сметану пролил… Да что и говорить — не останусь я больше за хозяйку, сама хозяйничай.<br>
            — Так я и знала, что ничего у тебя не получится!<br>
            — А батюшка пришёл нас на свадьбу приглашать, надо пойти! — Хома уже про все свои несчастья забыл.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/259_6.png" alt=""/>
            <figcaption>Источник: Юфа Михаил Шлемович</figcaption>
          </figure>
          <p>Кое-как прибрали, пошли на свадьбу. А там только ещё гости съезжаются, свадьба на завтра назначена. А сегодня
            суббота — надо баню истопить, да дров нет. Спрашивают:<br>
            — Кто поедет в лес за дровами? Хома кричит первым:<br>
            — Я!<br>
            Ну что же, дали ему хорошую лошадь, взял он топор и поехал. Выбрал в лесу дерево потолще да подлиннее и
            давай рубить. Думает: в какую бы сторону его свалить? Думал, думал и решил свалить дерево прямо на дугу —
            легче будет на телегу наваливать. Тюкал, тюкал полдня, наконец свалил дерево. Упало оно на дугу, а у лошади
            и ноги в стороны разъехались, она и не дышит уже.<br>
            — Что же это я опять натворил!<br><br>
            Сел на пень и давай плакать. Проплакал до самого вечера. А дома уже давно баню истопили, попарились,
            помылись, говорят:<br>
            — Что-то зятя Хомы долго нет?<br>
            Пошли искать. Приходят в лес, а он сидит на пне, слезами заливается.<br>
            — Что ты плачешь? — спрашивают у него.<br>
            Он и слова сказать не может, только всхлипывает.<br>
            — Перестань, поедем домой!<br>
            Посадили его на телегу и повезли. Приехали домой. Свадьба уже в разгаре. Овцу зарезали. Надо бы для гостей
            мясо сварить.<br>
            — Кто будет мясо варить?<br>
            Хома опять выскочил раньше всех:<br>
            — Я буду варить!<br>
            — Ну, вари.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/259_7.png" alt=""/>
            <figcaption>Источник: Юфа Михаил Шлемович</figcaption>
          </figure>
          <p>Сварил он мясо во дворе в большом котле, направился к дому, а собаки окружили его, лают. Он бросил им один
            кусок, второй — так всё мясо и разбросал, а собаки всё лают. Он и суп им выплеснул: «Нате, ешьте, только
            отвяжитесь от меня!» И пришёл домой с пустым котлом.<br>
            — А, зять Хома, уже сварил мясо? Где же оно?<br>
            — Не спрашивайте — собаки как напали на меня да залаяли, так я им всё мясо и скормил, а под конец и суп
            вылил!<br>
            — Авой-вой-вой, зять Хома, что ты опять наделал! Чем же мы гостей угощать будем?<br>
            Усадили его на копну осоки в огороде и говорят:<br>
            — Сиди тут и в избу не являйся!<br>
            Но не сидится Хоме, приходит в избу, садится на лавку среди гостей, как ни в чём не бывало. Хозяин
            говорит:<br>
            — Кто пойдёт за квасом? Бочка в амбаре стоит. Хома тут как тут:<br>
            — Я пойду!<br>
            — Ну, раз пойдёшь, так иди. Вот ведро.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/259_8.png" alt=""/>
            <figcaption>Источник: Юфа Михаил Шлемович</figcaption>
          </figure>
          <p>Взял Хома ведро, пошёл в амбар, вытащил затычку, квас течёт в ведро. Собаки опять на него залаяли, он и
            запустил в них затычкой! Квас уже через край льётся, а бочку заткнуть нечем. Что делать? Сунул Хома в
            отверстие палец, заткнул бочку. Стоит, ждёт, когда придут за ним. Палец распух, хоть караул кричи! А хозяин
            ждёт, когда Хома принесёт квасу для гостей. Ждали, ждали, наконец пошли за ним. Приходят, а Хома стоит у
            бочки и приплясывает: палец не может вытащить. Пришлось бочку разбить. Говорят Хоме:<br>
            — Чтоб больше в избе не показывался! Хватит с нас твоих выдумок!<br>
            Забрался Хома на крышу, заглядывает в отдушину, что на свадьбе делается.<br>
            А отдушина была большая, все края в густой саже, Хома вместе с хлопьями сажи и свалился с потолка. Невесту и
            всех гостей перепачкал.<br>
            — Ох, зять Хома, что ты ещё придумал, всю свадьбу испортил!<br>
            Опять его усадили на осоку и сказали:<br>
            — Чтоб больше с места не сдвинулся! Забрался Хома в сено и уснул—устал бедняга.<br>
            Гости припозднились, стали их на ночь оставлять. Пошла слепая старуха постельники сеном набивать и не
            заметила, как Хома потихоньку в постельник залез. Постелили гостям на полу, легли они вповалку спать. А Хома
            то одного щипнет, то другого пнёт.
          </p>
          <p>Гости думают, что кто-то из них в темноте проказничает, начали друг друга винить.<br>
            Один говорит:<br>
            — Что ты пинаешься? Другой говорит:<br>
            — Нет, это ты пнул!<br>
            Стали смотреть постели, распороли постельник, а там Хома. Отец и говорит дочери:<br>
            — Вези скорее своего Хому домой, если хочешь, чтоб он жив остался!<br>
            Посадили Хому на телегу, повезли домой, и гости поехали домой.<br>
            На этом кончилась свадьба, тут и сказке конец.</p>
