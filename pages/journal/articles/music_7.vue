                    <p>Сегодня любой желающий может насладиться звучанием сочинений эпохи барокко или классицизма в исполнении
                        аутентичных инструментов, однако звучание это будет отличаться. Начиная с середины XX столетия классическая
                        музыка исполняется на современных музыкальных инструментах, а это уже совсем иное звучание, не то, которое
                        создавал композитор. Рассмотрим кратко три музыкальных инструмента оркестра.</p>
                    <h2>Скрипка</h2>
                    <p>В середине XVI века скрипка приобрела характерную форму с четырьмя струнами, а в конце XVII — начале XVIII
                        веков была доведена до совершенства итальянскими мастерами Антонио Страдивари и Никколо Амати.</p>
                    <p>Мелодичная скрипка превосходит многие другие инструменты и позволяет музыканту исполнять сложные и виртуозные
                        пассажи благодаря чему она занимает лидирующую роль в оркестре начиная с эпохи Барокко. Именно поэтому
                        скрипки составляют большую часть оркестра, а ведущий музыкант-скрипач в отсутствие дирижёра проводит
                        репетиции.</p>
                    <figure class="small">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/201_4.png" alt=""/>
                        <figcaption>Источник: google.com</figcaption>
                    </figure>
                    <h2>Кларнет</h2>
                    <p>Предполагается, что в начале XVIII века Иоганн Кристоф Деннер работал над усовершенствованием старинного
                        французского духового инструмента шалмея и результатом работы стал кларнет. Однако исторические сведения
                        данного события сомнительны, но, так или иначе, группу деревянных духовых инструментов пополнил новый, без
                        которого не обходится ни один оркестр.</p>
                    <p>Кларнетами называются несколько инструментов разного размера и звучания. В классическом оркестре присутствует
                        два кларнета, однако, начиная с конца XIX века, создаются произведения, в которых требуется три и более
                        кларнета. Нередко кларнет выступает солирующим инструментом.</p>
                    <figure class="full">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/201_5.png" alt=""/>
                        <figcaption>Источник: google.com</figcaption>
                    </figure>
                    <h2>Рояль</h2>
                    <p>Первые попытки создания клавишного инструмента человечество предпринимало и раньше, так, в средние века
                        появилась колёсная лира. В <NuxtLink to="/articles/trubadur">позднем Средневековье</NuxtLink> появился клавесин, который широко использовался в музыке
                        Ренессанса и барокко. Правда в конце XVII начале XVIII века итальянцем Бартоломео Кристоферо инструмент был
                        усовершенствован и мир увидел новый музыкальный инструмент — рояль, который стал одним из самых популярных в
                        классической музыке.</p>
                    <p>На рояле играли многие великие композиторы — Шопен, Мендельсон, Дебюсси, Бетховен, Моцарт и другие.
                        Если рояль участвует в концерте, то его располагают впереди, перед дирижёром. К слову о клавесине, в XX
                        столетии произошло его возрождение и связано оно с новой модой на исполнение средневековой и барочной музыки
                        на аутентичных музыкальных инструментах.</p>
