                    <h1>Солирующие музыкальные инструменты оркестра</h1>
                    <hr>
                    <blockquote>«Оркестру никогда не найдут замены. Когда сто человек, каждый из которых индивидуальность,
                        складываются в единое звуковое поле, то всё прошибает насквозь»<cite>Эдуард Артемьев</cite></blockquote>
                    <hr>
                    <p>Пройден долгий путь по истории музыки начиная с <NuxtLink itemprop="url" to="/articles/music">доисторических и античных времён</NuxtLink>, эпохи
                        <NuxtLink to="/articles/music_1">Ренессанса</NuxtLink> и <NuxtLink to="/articles/music_2">Барокко</NuxtLink> и заканчивая эпохами <NuxtLink to="/articles/music_3">классицизма</NuxtLink> и <NuxtLink to="/articles/music_4">романтизма</NuxtLink>, которые подарили человечеству великих
                        композиторов и музыкантов.
                    </p>
                    <p>В XX столетии в музыке наступил <NuxtLink to="/articles/music_5">период диссонанса</NuxtLink>, в котором господствовали фашизм и коммунизм. Одно
                        направление в музыке сменялось другим в быстрых темпах. В начале века господствовал романтизм, который к
                        концу 20-х годов был вытеснен экспрессионизмом, который в 1945 году был отодвинут на задний план
                        неоклассицизмом.</p>
                    <figure class="small">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/201_2.png" alt=""/>
                        <figcaption>Источник: google.com</figcaption>
                    </figure>
                    <p>В столь бурное время композиторы активно обращались к народной музыке, изучали старинные инструменты и их
                        звучание, совершенствовали музыкальные инструменты и <NuxtLink to="/articles/music_6">организации оркестров</NuxtLink>. Такова история становления
                        классической музыки, но не хватает завершающего штриха в рассказе — истории развития основных музыкальных
                        инструментов. Все инструменты не удастся упомянуть, расскажем лишь о трёх.</p>
                    <p>Основу любого оркестра составляют ключевые музыкальные инструменты, которые необходимы для верного звучания.
                        Раскроем секрет — именно для ключевых инструментов великие композиторы и создавали свои произведения. Однако
                        за долгие годы и века инструменты претерпели кардинальные изменения.</p>
                    <figure class="full">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/201_3.png" alt=""/>
                        <figcaption>Источник: google.com</figcaption>
