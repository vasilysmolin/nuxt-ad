                    <p>Задумывались ли вы, на что расходуется жизнь и важный ресурс &mdash; время? Возьмем за основу средние показатели,
                        где продолжительность жизни человека составляет шестьдесят семь лет. Треть жизни каждый человек тратит
                        на то, чтобы расти, питаться и развлекаться. Еще треть жизни уходит на сон, а если болезнь, да
                        еще и продолжительная, думаем, вы догадываетесь о последствиях. Остальная часть жизни, а это примерно
                        двадцать лет, уходит у человека на активный труд. Однако обманчиво полагать, что все двадцать лет
                        активного труда являются продуктивными. Цифра эта относится к рабочим часам, которые отличаются
                        от продуктивных. Вот о продуктивности и пойдет речь в данной статье, а точнее о том, как рассчитать свой
                        продуктивный час времени. Возникает вопрос, а, собственно, зачем мне знать стоимость продуктивного
                        часа?</p>
                    <figure class="small">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/50_3.png" alt=""/>
                        <figcaption>Источник: blog.patternbank.com</figcaption>
