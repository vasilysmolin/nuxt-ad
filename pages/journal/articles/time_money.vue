                    <p>В различных книгах и тренингах по тайм-менеджменту часто встречаются разноцветные, привлекающие внимание,
                        схемы и графики по расчетам стоимости рабочего часа. Согласно формуле, сперва необходимо вычислить
                        количество рабочих часов за год — количество рабочих дней в году умножаем на восемь рабочих часов за
                        день. Далее берем желаемый доход, например, сто тысяч рублей в год и делим на количество всех рабочих
                        часов за год. Расчет стоимости часа будет таким — 100 000 / (220*8) = 56,82 рубля. Но не
                        торопитесь считать. Расчеты основаны на восьмичасовом рабочем дне, но ни один человек на отрабатывает
                        продуктивно все эти часы. Данная формула рассчитывает рабочие часы, а нас интересуют продуктивные.</p>
                    <p>Как же рассчитать эти продуктивные часы? Итак, как мы написали выше, ни один человек не отрабатывает
                        продуктивно все восемь рабочих часов. Размер продуктивного рабочего времени в среднем составляет три
                        часа. Да, всего лишь три часа, а у некоторых людей и того меньше. Например, лучшие генеральные директора
                        продуктивно работают не больше сорока пяти минут в день. Формула расчета продуктивного времени выглядит
                        так —</p>
                    <cite>желаемый базовый заработок делим на число рабочих часов в году и умножаем на коэффициент
                        продуктивности</cite>
                    <p>Мы знаем, что примерное продуктивное время, затрачиваемое на работу составляет три часа, поэтому
                        коэффициент продуктивности в нашем случае будет равен трем. В итоге расчет будет выглядеть так — 100 000
                        / (220*8)*3 = 170,45 рублей. Такова стоимость вашего продуктивного часа, а если разделить полученное
                        число на шестьдесят, то получите стоимость минуты.</p>
