                    <h1>Да, это банально: время &mdash; деньги</h1>
                    <hr>
                    <blockquote>«Нет ничего продолжительнее времени, так как оно мера вечности; нет ничего короче его, так
                        как его недостает для всех наших начинаний… Все люди пренебрегают им, все сожалеют о его утрате»<cite>Франсуа-Мари Вольтер — французский философ, поэт, прозаик и сатирик XVIII век</cite></blockquote>
