                    <p>Время — самая большая ценность для каждого человека, но особенно оно ценно для людей, стремящихся взять
                        от жизни все и даже больше. Предприниматели ценят время на вес золота. Каждый день они стремятся
                        выкроить какую-то часть его на изобретения, планирования, на сбор и обработку информации, на подъем
                        продаж, на умножение прибыли и многое другое. Эти люди знают истинную цену времени и не позволяют
                        тратить ценный ресурс на всякую ерунду. Разбрасываться временем для них — непозволительная роскошь,
                        которая имеет цену. Приведем формулу, по которой люди, ценящие время, рассчитывают свой продуктивный
                        час. Минуточку, не торопитесь с выводами, если вы не состоите в их рядах и вам незачем знать стоимость
                        своего времени. Думаем, вы измените свое мнение дочитав до конца, ну или, по крайней мере, призадумаетесь
                        над этим.</p>
