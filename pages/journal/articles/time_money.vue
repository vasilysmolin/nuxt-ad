                    <p>Полученное число может изменить всю вашу жизнь и это не пустые слова. Стоимость продуктивного часа,
                        казалось бы простое число, но сколько смысла оно содержит. Рассчитав стоимость своего продуктивного часа
                        и задумавшись над ним, начинаешь по-другому относиться ко времени. Держа постоянно цифру в голове,
                        каждый день осознавая, что она является двигателем к желаемым жизненным результатам, перестаешь
                        разбрасываться временем. Постепенно, а у кого-то и резко, меняются жизненные приоритеты. Человек,
                        ценящий свой временной ресурс, не тратит его бездумно на пустую болтовню, сплетни в курилке, пустые
                        обсуждения. Словом, не тратит свое время на обстоятельства, которые от него не зависят. Человек, ценящий
                        свой временной ресурс, уделяет внимание тем обстоятельствам, которые зависят только от него на все сто
                        процентов.</p>
