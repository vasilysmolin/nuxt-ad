                        трудней выбраться из этого состояния. Бывают моменты просветления, не спорим, но эти моменты коротки и
                        мимолетны. И, как правило, такие моменты служат маленькой передышкой, своеобразным островком спасения в
                        большом заплыве по жизни. Явление это обманчивое и не стройте иллюзий, что с завтрашнего дня жизнь
                        изменится. Не изменится, а все почему? Все потому, что чтобы что-то изменить, необходимо приложить
                        усилия и, как модно сейчас говорить — выйти из зоны комфорта. Справиться с этим могут лишь пять процентов
                        из ста.
                    </p>
