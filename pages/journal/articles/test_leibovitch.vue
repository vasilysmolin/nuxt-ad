          <hr>
          <p>Статистика такова, что более 30% людей в той или иной степени испытывают страх перед людьми или
            боязнь социальных ситуаций. Многие люди боятся публичных выступлений, взглядов прохожих на улице или
            заговорить с незнакомцем. Боятся находиться на улице, в общественном месте. Боязнь доходит до того, что
            человек не в состоянии заниматься чем-либо если за ним наблюдают со стороны другие люди.
          </p>
          <cite>Социофобия — навязчивый иррациональный страх перед выполнением действий на глазах у публики</cite>
          <p>Проявляться социофобия начинает в раннем возрасте и обнаруживается в одиннадцать лет. В одинаковой степени
            страдают ей и мужчины, и женщины, при чём не имеет значения ни возраст, ни род занятий человека.
            Возникновение социофобии неразрывно связано с неуверенностью в себе и скрыто глубоко в подсознании.
            Психологи полагают, что данная фобия берёт своё начало в детстве, когда малыш подвержен либо недостатку
            внимания, либо гиперопеке.
          </p>
          <figure class="small">
            <img src="../../../assets/img_journal_temp/238_2.png" alt=""/>
            <figcaption>Источник: google.com. Takashi Murakami.</figcaption>
          </figure>
          <p>В определённые жизненные ситуации у человека могут развиваться навязчивые переживания, которые и являются
            развитием социофобии. Например, многие люди сталкиваются с различными ситуациями, в которых боятся выглядеть
            нелепо или сказать какую-либо глупость. Особенно переживают из-за таких ситуаций дети и подростки. В столь
            юном возрасте психика ещё не окрепла и детский ум остро реагирует на различного рода критику.
          </p>
          <p>Возникновение болезни в столь раннем возрасте и игнорирование явных симптомов, в дальнейшем может повлечь за
            собой и другие расстройства: депрессию, злоупотребление алкоголем и наркотическими веществами. Обнаруживание
            симптомов на ранней стадии помогает минимизировать негатив и избежать развития дополнительных расстройств,
            например, депрессии.
          </p>
          <figure class="full">
            <img src="../../../assets/img_journal_temp/238_3.png" alt=""/>
            <figcaption>Источник: google.com. Takashi Murakami.</figcaption>
          </figure>
          <p>Оценить симптомы социофобии можно при помощи шкалы Лейбовича, разработанной в 1987 году психиатром и
            исследователем Майклом Лейбовичем из США. Лейбович первым обратил внимание и стал изучать «социофобию», о
            которой в то время никто и не задумывался. Именно шкала Лейбовича широко применяется для оценки реакции
            человека в различных жизненных ситуациях и для диагностики признаков социофобии, чтобы вовремя обратиться за
            помощью. Предлагаем пройти тест Лейбовича, который универсален для взрослых и подростков.
          </p>
          <p>Тест состоит из 24 вопросов, ответы на которые следует давать, опираясь на события прошедшей недели и по настоящий момент времени включительно. Внимательно прочитайте каждую ситуацию и ответьте по каждой из них два раза на вопросы: 1) насколько сильный страх, беспокойство или тревогу вы испытываете в данной ситуации; 2) как часто вы избегаете подобных ситуаций.</p>
