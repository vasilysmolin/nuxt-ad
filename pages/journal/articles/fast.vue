                        запястья и поясницы пряжей. Считалось, что такой обряд на весь год от болезней предохранит.</p>
                    <cite>Любое омовение в Чистый четверг означало приготовление к весеннему севу, приступать к которому необходимо
                        чистым и душой и телом</cite>
                    <p>С целью очищения использовался и обряд окуривания. На Русском Севере в Чистый четверг ходили в лес за
                        можжевельником. Зажигали его на сковороде и окуривали все углы в доме. Принято было и перепрыгивать через
                        дымящуюся сковороду. Бывало разжигали и большой можжевеловый костёр, через который перепрыгивали все
                        желающие дабы избавиться от грехов и не болеть.</p>
