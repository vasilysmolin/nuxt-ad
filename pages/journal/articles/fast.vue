                    <p>Пожалуй, самым распространённым обрядом был и остаётся обряд приготовления четверговой соли. Способов
                        приготовления было несколько. Первый, и самый простой, заключался в том, что соль просто ставили в ночь на
                        Чистый четверг на стол, где она приобретала магические чудодейственные свойства. Второй способ заготовления
                        также не требовал применения лишних действий — соль просто завертывали в узелок и укладывали в печь, а
                        иногда и просто прожигали её на углях.</p>
                    <cite>Четверговая соль — лекарство от всех болезней</cite>
                    <p>Четверговые обряды, пожалуй единственные, которые не испытали на себе церковного влияния и долгое время, а
                        некоторые и по сей день, используются. Проведя все очистительные четверговые обряды, люди встречали
                        Страстную или Великую субботу, а следом за ней и Пасху.</p>
                    <figure class="small">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/172_8.png" alt=""/>
                        <figcaption>Источник: google.com</figcaption>
