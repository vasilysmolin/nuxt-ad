                    <p>Соблюдение Великого поста таким образом сводится не только к телесному, когда под запретом определённая пища
                        и напитки, но и к душевному посту, когда человек воздерживается от злых помыслов, дел и слов. Такова
                        истинная ценность святого поста. Углубляться в церковные нравоучения и назидания более не будем. Предлагаем
                        ознакомиться с некоторыми приметами и обычаями Великого поста на Руси.</p>
                    <cite>Седмица — неделя, состоящая из семи суток</cite>
                    <p>Великий пост длится почти семь полных недель — сорок восемь дней и заканчивается Пасхой. Первая седмица
                        Великого поста в народе называется «Фёдорова неделя» и каждый день недели имеет свои приметы и обычаи.
                        Например, в четверг начинают улетать на север зимние птицы, а в пятницу во имя спасения души и тела принято
                        было употреблять в пищу только коливо с мёдом. В субботу тужат по Масленой и пекут блины на постном масле, а
                        в воскресенье отмечается Торжество православия.</p>
                    <figure class="full">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/172_3.png" alt=""/>
                        <figcaption>Источник: google.com</figcaption>
