                    <p>В Великую субботу святили куличи и яйца, а кое-где и иные продукты. В некоторых местах хозяйки готовили
                        пасхальные корзинки, которые обязательно включали в себя крашенные яйца, творожные пасхи и куличи. Согласно
                        традициям и обычаям в корзинки также клали кусок сливочного масла, запеченную свинину и соль. Свинина
                        выступала символом плодородия, а соль считалась энергией жизни.</p>
                    <p>В Великую субботу запрещалось заниматься шитьём, стиркой и уборкой, работать в огороде и рубить дрова.
                        Запрещалось ходить в этот день на охоту и рыбалку. Весь пост люди старались не сквернословить и не ругаться,
                        не обижать близких, не смеяться громко, а в Страстную субботу и подавно. В Великую субботу советовали не отмечать
                        день рождения, а перенести его на другой день.</p>
                    <cite>Ясный день в Страстную субботу к сухому и ясному лету</cite>
                    <p>В воскресенье завершается Великий семинедельный пост и по старинным обычаям семья собирается в родительском
                        доме в тесном кругу и встречает Пасху. Родные люди обмениваются куличами и крашенными яйцами, поздравляют
                        друг друга с Рождеством Христовым и отведывают приготовленные угощения.</p>
