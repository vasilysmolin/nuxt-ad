                        Великого поста молодожёны устраивали угощение для родин со стороны молодой жены. В среду провожают зиму, а в
                        субботу проходят званые поминки.</p>
                    <p>К моменту начала шестой недели Великого поста распускается верба, отсюда и название шестой недели — вербная.
                        В понедельник было принято, чтобы тёща приносила в дом молодого зятя подарки и желала семье добра и
                        благополучия. В Вербное воскресенье принято было освящать вербу.</p>
