                        печенье пеклось с сюрпризом внутри — внутрь печенья укладывали зёрна хлебных злаков, крестик, монетку и
                        другие символические предметы. Печенье следовало съесть в тот же день, как его испекли, а три дополнительных
                        креста оставляли до сева.</p>
                    <figure class="full">
                        <img src="https://storage.yandexcloud.net/backgrounds-images/journal_static/172_5.png" alt=""/>
                        <figcaption>Источник: google.com</figcaption>
                    </figure>
                    <p>В тот день, когда наступала пора сеять, поле обходили три раза, а затем разламывали печенье и съедали его.
                        Тому, кому посчастливилось заполучить сюрприз с печеньем — зерно, монетка — тот и начинал сев. Нередко
                        обрядовое печенье в виде крестов выступало и как оберег, и как целебное средство — их клали в закрома с
                        зерном или мукой.</p>
                    <p>Перелом поста наступает в среду и по народному поверью в этот день пост переламывается В переднем углу. В
