                    <h1>Приметы и обычаи Великого поста на Руси</h1>
                    <hr>
                    <blockquote>«... святой пост для всех действительно и истинно желающих и старающихся спасти свою душу, с
                        одной стороны — великий учитель, а с другой — великий помощник»<cite>Энтони
                            протоиерей Григорий Дьяченко</cite></blockquote>
                    <hr>
                    <p>Истинно верующие люди считают святой пост великим учителем. Они верят, что соблюдение святого
                        поста в скором времени открывает им истину, заключающуюся в том, что для сохранения телесного и душевного
                        здоровья нет нужды в излишествах. Истинно верующие люди полагают, что для здоровья не нужны, а порой и
                        весьма вредны изысканные кушанья, лакомства и сласти, горячительные напитки, шумное веселье и через чур
                        изысканная, то есть щегольская одежда.
                    </p>
                    <p>Соблюдение Великого поста отнюдь не сводится лишь к перечисленным ограничениям в еде, питье и щегольстве.
                        Истинное соблюдение поста начинается в первую очередь с языка, то есть в контроле речей своих от слов
                        праздных, гнилых и неподобных. Считается, что как только человек сможет обуздать свой язык, ему покориться и
                        всё тело. Иначе, как сказал протоиерей Григорий Дьяченко:</p>
                    <cite>«... язык твой, как дикий и свирепый конь, будет влачить тебя, вместе с постом твоим по дебрям лжи, злобы и лукавства»</cite>
