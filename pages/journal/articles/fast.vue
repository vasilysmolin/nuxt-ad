                    <p>В середине Великого поста, на четвёртой неделе, отмечается Средокрестье, называемое также Средопостье или
                        Преполовение. На неделе принято было печь обрядовое печенье на постном масле, а иногда и без него. Печенье
                        пекли в виде крестов и считалось, что в таком виде оно будет способствовать хорошему севу и урожаю. Крестцы
                        давали силу земле-кормилице, мужику-пахарю и животным — лошадям и волам.</p>
                    <cite>Обрядовое печенье называлось крестцы или хресты</cite>
                    <p>Обрядовое печенье в одних местах пекли в среду, а в других в четверг из ржаной муки, а на Украине из
