                    <p>Почему первая седмица поста называется «Фёдоровой неделей»? Согласно преданию император Юлиан Отступник
                        спустя пятьдесят лет, как был сожжён Феодор Тирон, надругался над христианами. В этот день он приказал
                        окропить все продукты на рынках идолопоклонной кровью. Однако задуманному злому умыслу помешал сам святой
                        Феодор, явившийся во сне к архиепископу Евдоксию. Едоксию было велено передать людям ничего не покупать на
                        рынках в этот день, а вкушать лишь коливо с мёдом.</p>
                    <cite>Коливо — обрядовая пища из варёной пшеницы</cite>
                    <p>Вторая седмица Великого поста связана со святителем Григорием Паламой. В старину по понедельникам женщины
                        перенизывали жемчуг, убирали головные уборы и вышивали фаты шёлком. На третьей неделе Великого поста
                        справляли обжорную пятницу и субботу. В один из этих дней молодой зять приглашал тестя в гости и угощал его
